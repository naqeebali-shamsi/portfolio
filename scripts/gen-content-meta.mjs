// Build-time content-metadata generator.
//
// Why: the homepage "Latest Writing" section and the /blog + /case-studies index
// pages need each post's `meta` (title, date, slug, tags...) synchronously, but
// they must NOT pull the compiled MDX *bodies* into the main bundle. If the
// registry statically imports the .mdx files (even via `import: 'meta'`), Rollup
// keeps every body in the main chunk and refuses to code-split them.
//
// So we extract just the `export const meta = {...}` object literal from each MDX
// file here and emit a plain-data `_meta.generated.ts`. The registry imports that
// (tiny) data for listings, and lazy-imports the bodies via `import.meta.glob`
// (dynamic only) so each post becomes its own on-demand chunk.
//
// Runs as the first step of `npm run build`. The generated files are committed so
// `vite dev` works without a pre-step; re-run this script after adding/editing a
// post's `meta` (or just run a build).

import { readFileSync, writeFileSync, readdirSync, existsSync } from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));

/** Extract the `{...}` object literal following `export const meta =`,
 *  respecting string literals so braces inside prose don't break matching. */
function extractMetaLiteral(src) {
  const marker = 'export const meta';
  const start = src.indexOf(marker);
  if (start === -1) return null;
  const eq = src.indexOf('=', start + marker.length);
  if (eq === -1) return null;
  const open = src.indexOf('{', eq);
  if (open === -1) return null;

  let depth = 0;
  let inStr = null;
  let esc = false;
  for (let i = open; i < src.length; i++) {
    const c = src[i];
    if (esc) { esc = false; continue; }
    if (inStr) {
      if (c === '\\') esc = true;
      else if (c === inStr) inStr = null;
      continue;
    }
    if (c === '"' || c === "'" || c === '`') { inStr = c; continue; }
    if (c === '{') depth++;
    else if (c === '}') {
      depth--;
      if (depth === 0) return src.slice(open, i + 1);
    }
  }
  return null;
}

function loadMeta(file) {
  const src = readFileSync(file, 'utf8');
  const literal = extractMetaLiteral(src);
  if (!literal) throw new Error(`No \`export const meta\` object found in ${file}`);
  // The meta block is a pure JS object literal (no references/calls), so evaluating
  // it in a sandboxed Function is safe and handles single quotes, trailing commas,
  // template strings, and apostrophes that JSON can't.
  // eslint-disable-next-line no-new-func
  return Function(`"use strict"; return (${literal});`)();
}

function genDir(dir, exportName, typeName) {
  if (!existsSync(dir)) {
    console.warn(`[gen-content-meta] skip (missing): ${dir}`);
    return;
  }
  const files = readdirSync(dir)
    .filter((f) => f.endsWith('.mdx') && !f.startsWith('_'))
    .sort();

  const entries = [];
  for (const f of files) {
    const meta = loadMeta(path.join(dir, f));
    if (!meta || !meta.slug) {
      console.warn(`[gen-content-meta] WARNING: ${f} has no meta.slug — skipped`);
      continue;
    }
    entries.push({ path: './' + f, meta });
  }

  const out = `// AUTO-GENERATED by scripts/gen-content-meta.mjs — do not edit by hand.
// Re-run \`node scripts/gen-content-meta.mjs\` after changing a post's \`meta\`
// (this also runs automatically as the first step of \`npm run build\`).
import type { ${typeName} } from './registry';

export const ${exportName}: { path: string; meta: ${typeName} }[] = ${JSON.stringify(entries, null, 2)};
`;
  const outPath = path.join(dir, '_meta.generated.ts');
  writeFileSync(outPath, out, 'utf8');
  console.log(`[gen-content-meta] ${path.relative(process.cwd(), outPath)} — ${entries.length} entries`);
}

const root = path.resolve(__dirname, '..', 'src', 'content');
genDir(path.join(root, 'blog'), 'blogMetaEntries', 'BlogPostMeta');
genDir(path.join(root, 'case-studies'), 'caseStudyMetaEntries', 'CaseStudyMeta');
